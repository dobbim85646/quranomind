# QuranoMind - ذكاء اصطناعي لتفسير الآيات الكونية من القرآن

# قاعدة بيانات مصغّرة من الكلمات القرآنية المرتبطة بالكون
quran_data = {
    "الخنس": {
        "تفسير": "نجوم تختفي في السماء لا تُرى بالعين.",
        "علم": "ثقب أسود - Black Hole",
        "شرح": "جسم فضائي كثيف جدًا لا يخرج منه الضوء، يبتلع كل شيء حوله."
    },
    "الكنس": {
        "تفسير": "تكنس كل شيء يمر قربها",
        "علم": "جاذبية قوية جدًا",
        "شرح": "تسحب الأجسام حولها وتبتلعها بفعل الجاذبية."
    }
}

# طلب إدخال كلمة
word = input("🔍 أدخل كلمة قرآنية لفهم معناها العلمي: ")

# عرض النتيجة
if word in quran_data:
    print("\n📖 التفسير:", quran_data[word]["تفسير"])
    print("🔬 العلم:", quran_data[word]["علم"])
    print("🧠 الشرح العلمي:", quran_data[word]["شرح"])
else:
    print("\n❌ الكلمة غير موجودة في قاعدة البيانات حالياً.")
